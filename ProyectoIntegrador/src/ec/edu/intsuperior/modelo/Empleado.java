package ec.edu.intsuperior.modelo;

public class Empleado {

    private int id;
    private String cedEmp;
    private String nomEmp;
    private String edadEmp;
    private String dirEmp;
    private String carEmp;
    private String estadoEmp;
    private String correoEmp;
    private String telEmp;
    private String horaEmp;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getCedEmp() {
        return cedEmp;
    }

    public void setCedEmp(String cedEmp) {
        this.cedEmp = cedEmp;
    }

    public String getNomEmp() {
        return nomEmp;
    }

    public void setNomEmp(String nomEmp) {
        this.nomEmp = nomEmp;
    }

    public String getEdadEmp() {
        return edadEmp;
    }

    public void setEdadEmp(String edadEmp) {
        this.edadEmp = edadEmp;
    }

    public String getDirEmp() {
        return dirEmp;
    }

    public void setDirEmp(String dirEmp) {
        this.dirEmp = dirEmp;
    }

    public String getCarEmp() {
        return carEmp;
    }

    public void setCarEmp(String carEmp) {
        this.carEmp = carEmp;
    }

    public String getEstadoEmp() {
        return estadoEmp;
    }

    public void setEstadoEmp(String estadoEmp) {
        this.estadoEmp = estadoEmp;
    }

    public String getCorreoEmp() {
        return correoEmp;
    }

    public void setCorreoEmp(String correoEmp) {
        this.correoEmp = correoEmp;
    }

    public String getTelEmp() {
        return telEmp;
    }

    public void setTelEmp(String telEmp) {
        this.telEmp = telEmp;
    }

    public String getHoraEmp() {
        return horaEmp;
    }

    public void setHoraEmp(String horaEmp) {
        this.horaEmp = horaEmp;
    }
    
    
    
    
}
